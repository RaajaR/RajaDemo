package NumberPrograms;

public class swap_two_numbers_using_third_variable {
	
	public static void main (String[]args) {
		
		
		float f1=1.20f;
		float f2=2.45f;
		System.out.println("Before swap" + f1);
		System.out.println("After Swap" + f2);
		float temp= f1;
		f1=f2;
		System.out.println("After Swap" + f1);
		
		f2= temp;
		System.out.println("After Swap" + f2);
		
		
		
		
		
	}

}
